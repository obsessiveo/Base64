/**
 * @description Creates a lookup table for the given alphabet. This function is not used in the library, but it's here for reference.
 * @param alphabet is the alphabet to use for the lookup table
 * @returns A lookup table for the given alphabet.
 * The lookup table is a Uint8Array of length 256.
 * The index of the array is the ascii code of the character.
 * The value of the array is the index of the character in the alphabet.
 */
function createLookupTable(alphabet: string): Uint8Array {
  const table = new Uint8Array(256);
  for (let i = 0; i < alphabet.length; i++) {
    table[alphabet.charCodeAt(i)] = i;
  }
  return table;
}
